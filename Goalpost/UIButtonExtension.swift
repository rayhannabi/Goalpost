import UIKit

extension UIButton {
    func setSelectedColor() {
        self.backgroundColor = #colorLiteral(red: 0.2963824272, green: 0.7735554576, blue: 0.4626437426, alpha: 1)
    }
    
    func setDeselectedColor() {
        self.backgroundColor = #colorLiteral(red: 0.6274509804, green: 0.841670692, blue: 0.7027204006, alpha: 1)
    }
}
